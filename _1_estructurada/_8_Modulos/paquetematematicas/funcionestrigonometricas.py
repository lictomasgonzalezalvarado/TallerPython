
def seno(co:float, h:float)->float:
    return co/h

def coseno(ca:float, h:float)-> float:
    return ca/h

def tangente(co:float, ca:float)-> float:
    return co/ca

def cosecante(h:float, co:float)-> float:
    return h/co

def secante(h:float, ca:float) -> float:
    return h/ca

def cotangente(ca: float, co: float) -> float:
    return ca/co